/**
 * Seed system dictionaries to public schema
 */
export async function seedSystemDictionary(prisma: PrismaClient): Promise<void> {
    console.log('  → Seeding system dictionaries...');
  
    // Create dictionary types
    for (const dictType of DICTIONARY_TYPES) {
      await prisma.$executeRawUnsafe(`
        INSERT INTO public.system_dictionary (id, code, name_en, name_zh, name_ja, description_en, sort_order, is_active, created_at, updated_at, version)
        VALUES (gen_random_uuid(), $1, $2, $3, $4, $5, $6, true, now(), now(), 1)
        ON CONFLICT (code) DO UPDATE SET 
          name_en = EXCLUDED.name_en,
          name_zh = EXCLUDED.name_zh,
          name_ja = EXCLUDED.name_ja,
          description_en = EXCLUDED.description_en,
          sort_order = EXCLUDED.sort_order,
          updated_at = now()
      `, dictType.code, dictType.nameEn, dictType.nameZh, dictType.nameJa, dictType.descriptionEn || null, dictType.sortOrder);
    }
    console.log(`    ✓ Created ${DICTIONARY_TYPES.length} dictionary types`);
  
    // Create dictionary items for each type
    for (const [dictCode, items] of Object.entries(DICTIONARY_ITEMS)) {
      let insertedCount = 0;
      for (let i = 0; i < items.length; i++) {
        const item = items[i];
        const sortOrder = item.sortOrder ?? i + 1;
        const extraDataJson = item.extraData ? JSON.stringify(item.extraData) : null;
  
        await prisma.$executeRawUnsafe(`
          INSERT INTO public.system_dictionary_item (id, dictionary_code, code, name_en, name_zh, name_ja, sort_order, is_active, extra_data, created_at, updated_at, version)
          VALUES (gen_random_uuid(), $1, $2, $3, $4, $5, $6, true, $7::jsonb, now(), now(), 1)
          ON CONFLICT (dictionary_code, code) DO UPDATE SET 
            name_en = EXCLUDED.name_en,
            name_zh = EXCLUDED.name_zh,
            name_ja = EXCLUDED.name_ja,
            sort_order = EXCLUDED.sort_order,
            extra_data = EXCLUDED.extra_data,
            updated_at = now()
        `, dictCode, item.code, item.nameEn, item.nameZh || null, item.nameJa || null, sortOrder, extraDataJson);
        insertedCount++;
      }
      console.log(`    ✓ ${dictCode}: ${insertedCount} items`);
    }
  
    console.log('  ✓ System dictionaries seeded successfully');
  }