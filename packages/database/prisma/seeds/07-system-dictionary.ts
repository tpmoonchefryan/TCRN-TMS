/**
 * Seed system dictionaries to public schema
 */
export async function seedSystemDictionary(prisma: PrismaClient): Promise<void> {
    console.log('  → Seeding system dictionaries...');
  
    // Create dictionary types
    for (const dictType of DICTIONARY_TYPES) {
      await prisma.$executeRawUnsafe(`
        INSERT INTO public.system_dictionary (id, code, name, description, sort_order, is_active, created_at, updated_at, version)
        VALUES (gen_random_uuid(), $1, $2::jsonb, $3::jsonb, $4, true, now(), now(), 1)
        ON CONFLICT (code) DO UPDATE SET 
          name = EXCLUDED.name,
          description = EXCLUDED.description,
          sort_order = EXCLUDED.sort_order,
          updated_at = now()
      `, dictType.code, JSON.stringify(dictType.name), JSON.stringify(dictType.description ?? EMPTY_LOCALIZED_TEXT), dictType.sortOrder);
    }
    console.log(`    ✓ Created ${DICTIONARY_TYPES.length} dictionary types`);
  
    // Create dictionary items for each type
    for (const [dictCode, items] of Object.entries(DICTIONARY_ITEMS)) {
      let insertedCount = 0;
      for (let i = 0; i < items.length; i++) {
        const item = items[i];
        const sortOrder = item.sortOrder ?? i + 1;
        const extraDataJson = item.extraData ? JSON.stringify(item.extraData) : null;
  
        await prisma.$executeRawUnsafe(`
          INSERT INTO public.system_dictionary_item (id, dictionary_code, code, name, description, sort_order, is_active, extra_data, created_at, updated_at, version)
          VALUES (gen_random_uuid(), $1, $2, $3::jsonb, $4::jsonb, $5, true, $6::jsonb, now(), now(), 1)
          ON CONFLICT (dictionary_code, code) DO UPDATE SET 
            name = EXCLUDED.name,
            description = EXCLUDED.description,
            sort_order = EXCLUDED.sort_order,
            extra_data = EXCLUDED.extra_data,
            updated_at = now()
        `, dictCode, item.code, JSON.stringify(item.name), JSON.stringify(item.description ?? EMPTY_LOCALIZED_TEXT), sortOrder, extraDataJson);
        insertedCount++;
      }
      console.log(`    ✓ ${dictCode}: ${insertedCount} items`);
    }

    await prisma.systemDictionaryItem.updateMany({
      where: {
        dictionaryCode: 'languages',
        code: { in: [...DEPRECATED_LANGUAGE_CODES] },
      },
      data: {
        isActive: false,
        updatedAt: new Date(),
      },
    });
  
    console.log('  ✓ System dictionaries seeded successfully');
  }
